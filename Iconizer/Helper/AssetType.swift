///  Possible asset types to generate asset catalogs for.
///
///  - AppIcon:     Represents the AppIcon model
///  - ImageSet:    Represents the ImageSet model
///  - LaunchImage: Represents the LaunchImage model
enum AssetType: Int {
    case appIcon = 0
    case imageSet = 1
    case launchImage = 2
}
